add("## 2. REST endpoints by router")
add("")
add("Total: 141 endpoints across 15 router files. Mount prefixes reflect")
add("how the server in src/main/server.ts attaches them.")
add("")
total_routes = 0
for f in sorted(os.listdir('src/main/routes')):
    if not f.endswith('.ts'): continue
    text = open('src/main/routes/' + f).read()
    methods = re.findall(r"router\.(get|post|put|delete|patch)\(\s*['\"`]([^'\"`]+)['\"`]", text)
    if not methods: continue
    n = len(methods)
    total_routes += n
    add("### src/main/routes/" + f + "  (" + str(n) + " endpoints)")
    add("")
    add("| Method | Path |")
    add("|---|---|")
    for m, p in methods:
        add("| " + m.upper() + " | `" + p + "` |")
    add("")
add("Total: " + str(total_routes) + " endpoints.")
add("")
add("---")
