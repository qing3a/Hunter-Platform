add("## 3. Declared capabilities by role")
add("")
add("Capabilities are the typed contract. Each maps to a route + method,")
add("resolved at request time by the capability-resolver middleware.")
add("Total: 86 capabilities across 8 role sets.")
add("")
total_caps = 0
for f in sorted(os.listdir('src/main/capabilities')):
    if not f.endswith('.ts') or f in ('types.ts', 'index.ts'): continue
    text = open('src/main/capabilities/' + f).read()
    role_m = re.search(r"role:\s*'([^']+)'", text)
    if not role_m: continue
    role = role_m.group(1)
    name_matches = re.findall(r"name:\s*'([^']+)',", text)
    if not name_matches: continue
    n = len(name_matches)
    total_caps += n
    add("### Role `" + role + "`  (" + str(n) + " capabilities, src/main/capabilities/" + f + ")")
    add("")
    for n2 in name_matches:
        add("- `" + n2 + "`")
    add("")
add("Total: " + str(total_caps) + " capabilities.")
add("")
